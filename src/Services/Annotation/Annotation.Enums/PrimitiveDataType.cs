namespace PreciPoint.Ims.Services.Annotation.Enums;

/// <summary>
/// Enum representation of primitive data types of the C# language, not all types are represented, but may be added
/// </summary>
public enum PrimitiveDataType : byte
{
    /// <summary>
    /// Float
    /// </summary>
    Float,

    /// <summary>
    /// Double
    /// </summary>
    Double,

    /// <summary>
    /// signed integer 64 bit (long)
    /// </summary>
    Int64,

    /// <summary>
    /// signed integer 32 bit (int)
    /// </summary>
    Int32,

    /// <summary>
    /// signed integer 16 bit (sshort)
    /// </summary>
    Int16,

    /// <summary>
    /// signed integer 8 bit (sbyte)
    /// </summary>
    Int8,

    /// <summary>
    /// unsigned integer 64 bit (ulong)
    /// </summary>
    UInt64,

    /// <summary>
    /// unsigned integer 32 bit (uint)
    /// </summary>
    UInt32,

    /// <summary>
    /// unsigned integer 16 bit (ushort)
    /// </summary>
    UInt16,

    /// <summary>
    /// unsigned integer 16 bit (byte)
    /// </summary>
    UInt8
}